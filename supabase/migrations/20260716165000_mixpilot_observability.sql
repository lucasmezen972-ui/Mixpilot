-- MixPilot cloud observability schema
-- Source-controlled on GitHub; deployed to a dedicated Supabase Postgres project.

create extension if not exists pgcrypto;

create schema if not exists mixpilot_private;

create table if not exists public.mixpilot_devices (
    id uuid primary key default gen_random_uuid(),
    owner_id uuid not null references auth.users(id) on delete cascade,
    installation_id uuid not null,
    device_name text,
    platform text not null default 'macOS',
    app_version text,
    rekordbox_version text,
    last_seen_at timestamptz not null default now(),
    created_at timestamptz not null default now(),
    unique (owner_id, installation_id)
);

create table if not exists public.mixpilot_sessions (
    id uuid primary key default gen_random_uuid(),
    owner_id uuid not null references auth.users(id) on delete cascade,
    device_id uuid not null references public.mixpilot_devices(id) on delete cascade,
    started_at timestamptz not null default now(),
    ended_at timestamptz,
    app_version text,
    dj_backend text,
    rekordbox_version text,
    live_mode boolean not null default false,
    metadata jsonb not null default '{}'::jsonb
);

create table if not exists public.mixpilot_events (
    id bigint generated by default as identity primary key,
    owner_id uuid not null references auth.users(id) on delete cascade,
    device_id uuid not null references public.mixpilot_devices(id) on delete cascade,
    session_id uuid references public.mixpilot_sessions(id) on delete set null,
    occurred_at timestamptz not null default now(),
    category text not null,
    name text not null,
    severity text not null default 'info' check (severity in ('debug','info','warning','error','critical')),
    payload jsonb not null default '{}'::jsonb,
    client_event_id uuid not null,
    unique (owner_id, client_event_id)
);

create table if not exists public.rekordbox_validation_reports (
    id uuid primary key default gen_random_uuid(),
    owner_id uuid not null references auth.users(id) on delete cascade,
    device_id uuid not null references public.mixpilot_devices(id) on delete cascade,
    rekordbox_version text not null,
    controller_name text not null,
    preset_signature text not null,
    critical_ready boolean not null default false,
    completion_ratio double precision not null default 0 check (completion_ratio between 0 and 1),
    passed_ratio double precision not null default 0 check (passed_ratio between 0 and 1),
    report jsonb not null,
    validated_at timestamptz not null default now(),
    unique (owner_id, device_id, rekordbox_version, preset_signature)
);

create table if not exists public.mixpilot_commands (
    id uuid primary key default gen_random_uuid(),
    owner_id uuid not null references auth.users(id) on delete cascade,
    device_id uuid not null references public.mixpilot_devices(id) on delete cascade,
    created_at timestamptz not null default now(),
    expires_at timestamptz not null,
    command text not null,
    payload jsonb not null default '{}'::jsonb,
    status text not null default 'pending' check (status in ('pending','accepted','completed','failed','expired')),
    completed_at timestamptz,
    result jsonb
);

create index if not exists mixpilot_devices_owner_last_seen_idx
    on public.mixpilot_devices (owner_id, last_seen_at desc);
create index if not exists mixpilot_sessions_owner_started_idx
    on public.mixpilot_sessions (owner_id, started_at desc);
create index if not exists mixpilot_events_owner_time_idx
    on public.mixpilot_events (owner_id, occurred_at desc);
create index if not exists mixpilot_events_session_time_idx
    on public.mixpilot_events (session_id, occurred_at desc);
create index if not exists mixpilot_events_category_name_idx
    on public.mixpilot_events (category, name, occurred_at desc);
create index if not exists mixpilot_commands_device_status_idx
    on public.mixpilot_commands (device_id, status, created_at);

