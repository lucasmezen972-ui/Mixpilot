-- Commands are deliberately not insertable from the desktop client.
-- An authenticated admin/dashboard or a JWT-protected Edge Function can enqueue
-- allowlisted commands after checking ownership and expiry.

create or replace view public.mixpilot_device_health
with (security_invoker = true)
as
select
    d.owner_id,
    d.id as device_id,
    d.device_name,
    d.app_version,
    d.rekordbox_version,
    d.last_seen_at,
    (now() - d.last_seen_at) < interval '2 minutes' as online,
    count(e.id) filter (where e.severity in ('error','critical') and e.occurred_at > now() - interval '24 hours') as errors_last_24h,
    max(e.occurred_at) as last_event_at
from public.mixpilot_devices d
left join public.mixpilot_events e on e.device_id = d.id
group by d.owner_id, d.id;

grant select on public.mixpilot_device_health to authenticated;
