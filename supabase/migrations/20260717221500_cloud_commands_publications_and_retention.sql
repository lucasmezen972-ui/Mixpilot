create or replace function mixpilot_private.is_allowed_distribution_url(value text)
returns boolean
language sql
immutable
strict
set search_path = pg_catalog
as $$
    select value ~ '^https://github[.]com/lucasmezen972-ui/Mixpilot/releases(/|$)'
        or value ~ '^https://objects[.]githubusercontent[.]com/'
        or value ~ '^https://cqppkklfugbixpxwitab[.]supabase[.]co/storage/v1/object/public/mixpilot-(releases|mappings)/';
$$;

revoke all on function mixpilot_private.is_allowed_distribution_url(text)
    from public, anon, authenticated;

create or replace function public.mixpilot_guard_release_publication()
returns trigger
language plpgsql
set search_path = pg_catalog, public, mixpilot_private
as $$
begin
    if new.status = 'published' then
        if new.published_at is null or new.rollout_percentage <= 0 then
            raise exception 'Published releases require published_at and rollout_percentage > 0';
        end if;
        if new.signature is null or length(new.signature) < 32 then
            raise exception 'Published releases require an independent publisher signature';
        end if;
        if not mixpilot_private.is_allowed_distribution_url(new.download_url) then
            raise exception 'Release download URL is not on the MixPilot allowlist';
        end if;
        if new.release_page_url is not null
           and not mixpilot_private.is_allowed_distribution_url(new.release_page_url) then
            raise exception 'Release page URL is not on the MixPilot allowlist';
        end if;
    end if;
    return new;
end;
$$;

revoke all on function public.mixpilot_guard_release_publication()
    from public, anon, authenticated;

drop trigger if exists mixpilot_guard_release_publication
    on public.mixpilot_releases;
create trigger mixpilot_guard_release_publication
before insert or update on public.mixpilot_releases
for each row execute function public.mixpilot_guard_release_publication();

create or replace function public.mixpilot_guard_mapping_publication()
returns trigger
language plpgsql
set search_path = pg_catalog, public
as $$
begin
    if new.status = 'published' then
        if new.published_at is null or new.rollout_percentage <= 0 then
            raise exception 'Published mappings require published_at and rollout_percentage > 0';
        end if;
        if new.publisher_signature is null or length(new.publisher_signature) < 32 then
            raise exception 'Published mappings require an independent publisher signature';
        end if;
        if new.source_repository is distinct from 'lucasmezen972-ui/Mixpilot'
           or new.source_commit_sha is null
           or new.source_manifest_path is null
           or new.source_manifest_sha256 is null then
            raise exception 'Published mappings require trusted immutable GitHub provenance';
        end if;
        if new.generated_preset_sha256 is null then
            raise exception 'Published mappings require the generated preset digest';
        end if;
        if coalesce(new.validation_summary->>'unit_tests', '') <> 'passed'
           or coalesce(new.validation_summary->>'release_build', '') <> 'passed'
           or coalesce(new.validation_summary->>'dmg_checksum', '') <> 'passed' then
            raise exception 'Published mappings require completed CI validation';
        end if;
        if new.channel = 'stable'
           and coalesce(new.validation_summary->>'device_validation', '') <> 'passed' then
            raise exception 'Stable mappings require real device validation';
        end if;
    end if;
    return new;
end;
$$;

revoke all on function public.mixpilot_guard_mapping_publication()
    from public, anon, authenticated;
grant execute on function public.mixpilot_guard_mapping_publication()
    to service_role;

-- Client telemetry never becomes a global incident unless an administrator has
-- explicitly allowlisted the exact category/name pair.
create table if not exists mixpilot_private.incident_event_allowlist (
    category text not null,
    name text not null,
    enabled boolean not null default true,
    created_at timestamptz not null default now(),
    primary key (category, name)
);

revoke all on mixpilot_private.incident_event_allowlist
    from public, anon, authenticated;

create or replace function mixpilot_private.aggregate_event_incident()
returns trigger
language plpgsql
security definer
set search_path = pg_catalog, public, mixpilot_private
as $$
declare
    incident_fingerprint text;
begin
    if new.severity not in ('error', 'critical') then
        return new;
    end if;
    if not exists (
        select 1
        from mixpilot_private.incident_event_allowlist a
        where a.category = new.category
          and a.name = new.name
          and a.enabled
    ) then
        return new;
    end if;

    incident_fingerprint := encode(
        extensions.digest(convert_to(new.category || ':' || new.name, 'UTF8'), 'sha256'),
        'hex'
    );

    insert into public.mixpilot_incidents (
        fingerprint, category, name, severity, status, occurrences,
        affected_devices, first_seen_at, last_seen_at, safe_summary
    ) values (
        incident_fingerprint, left(new.category, 80), left(new.name, 80),
        new.severity, 'open', 1, 1, new.occurred_at, new.occurred_at,
        left(new.category || ' / ' || new.name, 180)
    )
    on conflict (fingerprint) do update
    set occurrences = public.mixpilot_incidents.occurrences + 1,
        severity = case
            when excluded.severity = 'critical' then 'critical'
            else public.mixpilot_incidents.severity
        end,
        last_seen_at = greatest(public.mixpilot_incidents.last_seen_at, excluded.last_seen_at),
        updated_at = now();

    return new;
end;
$$;

revoke all on function mixpilot_private.aggregate_event_incident()
    from public, anon, authenticated;

create or replace function mixpilot_private.purge_expired_data(p_batch_size integer default 5000)
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, public
as $$
declare
    event_count integer := 0;
    command_count integer := 0;
begin
    with expired as (
        select id from public.mixpilot_events
        where expires_at <= now()
        order by expires_at
        limit least(greatest(coalesce(p_batch_size, 5000), 1), 20000)
    )
    delete from public.mixpilot_events e
    using expired x
    where e.id = x.id;
    get diagnostics event_count = row_count;

    with expired as (
        select id from public.mixpilot_commands
        where expires_at <= now()
          and (completed_at is null or completed_at < now() - interval '7 days')
        order by expires_at
        limit least(greatest(coalesce(p_batch_size, 5000), 1), 20000)
    )
    delete from public.mixpilot_commands c
    using expired x
    where c.id = x.id;
    get diagnostics command_count = row_count;

    return jsonb_build_object(
        'events_deleted', event_count,
        'commands_deleted', command_count
    );
end;
$$;

revoke all on function mixpilot_private.purge_expired_data(integer)
    from public, anon, authenticated;
grant execute on function mixpilot_private.purge_expired_data(integer)
    to service_role;

create extension if not exists pg_cron with schema extensions;

do $$
declare
    existing_job bigint;
begin
    for existing_job in
        select jobid from cron.job where jobname = 'mixpilot-purge-expired-data'
    loop
        perform cron.unschedule(existing_job);
    end loop;

    perform cron.schedule(
        'mixpilot-purge-expired-data',
        '17 4 * * *',
        $cron$select mixpilot_private.purge_expired_data(5000);$cron$
    );
end
$$;
