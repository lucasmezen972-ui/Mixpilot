-- Harden maintenance commands, publication metadata, telemetry aggregation and retention.
-- Apple Developer ID and notarization are deliberately not required here.

alter table public.mixpilot_commands
    add column if not exists idempotency_key uuid not null default gen_random_uuid(),
    add column if not exists sequence_number bigint generated by default as identity,
    add column if not exists claimed_at timestamptz,
    add column if not exists claimed_by_instance text,
    add column if not exists attempt_count integer not null default 0,
    add column if not exists failure_code text;

alter table public.mixpilot_commands
    add constraint mixpilot_commands_device_idempotency_key
        unique (device_id, idempotency_key),
    add constraint mixpilot_commands_claim_fields_check
        check (
            (claimed_at is null and claimed_by_instance is null)
            or (claimed_at is not null and length(claimed_by_instance) between 8 and 160)
        ),
    add constraint mixpilot_commands_attempt_count_check
        check (attempt_count between 0 and 20),
    add constraint mixpilot_commands_expiry_window_check
        check (expires_at > created_at and expires_at <= created_at + interval '1 day'),
    add constraint mixpilot_commands_payload_size_check
        check (octet_length(payload::text) <= 16384),
    add constraint mixpilot_commands_result_size_check
        check (result is null or octet_length(result::text) <= 16384);

create index if not exists mixpilot_commands_claimable_idx
    on public.mixpilot_commands (device_id, status, sequence_number)
    where claimed_at is null;

create or replace function public.claim_mixpilot_commands(
    p_device_id uuid,
    p_instance_id text,
    p_limit integer default 10
)
returns setof public.mixpilot_commands
language plpgsql
security definer
set search_path = pg_catalog, public
as $$
begin
    if auth.uid() is null then
        raise exception 'Authentication required' using errcode = '42501';
    end if;
    if p_instance_id is null or length(p_instance_id) not between 8 and 160 then
        raise exception 'Invalid instance identifier' using errcode = '22023';
    end if;

    return query
    with candidates as (
        select c.id
        from public.mixpilot_commands c
        where c.owner_id = auth.uid()
          and c.device_id = p_device_id
          and c.status = 'pending'
          and c.claimed_at is null
          and c.expires_at > now()
        order by c.sequence_number
        for update skip locked
        limit least(greatest(coalesce(p_limit, 1), 1), 10)
    )
    update public.mixpilot_commands c
       set status = 'accepted',
           claimed_at = now(),
           claimed_by_instance = p_instance_id,
           attempt_count = c.attempt_count + 1
      from candidates
     where c.id = candidates.id
    returning c.*;
end;
$$;

revoke all on function public.claim_mixpilot_commands(uuid, text, integer)
    from public, anon;
grant execute on function public.claim_mixpilot_commands(uuid, text, integer)
    to authenticated, service_role;

create or replace function public.complete_mixpilot_command(
    p_command_id uuid,
    p_instance_id text,
    p_succeeded boolean,
    p_result jsonb default '{}'::jsonb,
    p_failure_code text default null
)
returns boolean
language plpgsql
security definer
set search_path = pg_catalog, public
as $$
declare
    updated_count integer;
begin
    if auth.uid() is null then
        raise exception 'Authentication required' using errcode = '42501';
    end if;
    if p_instance_id is null or length(p_instance_id) not between 8 and 160 then
        raise exception 'Invalid instance identifier' using errcode = '22023';
    end if;
    if octet_length(coalesce(p_result, '{}'::jsonb)::text) > 16384 then
        raise exception 'Command result is too large' using errcode = '22023';
    end if;

    update public.mixpilot_commands
       set status = case when p_succeeded then 'completed' else 'failed' end,
           completed_at = now(),
           result = coalesce(p_result, '{}'::jsonb),
           failure_code = case when p_succeeded then null else left(p_failure_code, 120) end
     where id = p_command_id
       and owner_id = auth.uid()
       and status = 'accepted'
       and claimed_by_instance = p_instance_id
       and completed_at is null;

    get diagnostics updated_count = row_count;
    return updated_count = 1;
end;
$$;

revoke all on function public.complete_mixpilot_command(uuid, text, boolean, jsonb, text)
    from public, anon;
grant execute on function public.complete_mixpilot_command(uuid, text, boolean, jsonb, text)
    to authenticated, service_role;

